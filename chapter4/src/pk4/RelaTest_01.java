package pk4;

public class RelaTest_01 {

	public static void main(String[] args) {

		char a,b;
		a='A';//char 는 '' String "", A의 아스키코드값은 65
		b='B';
		
		System.out.println("a>b : " + (a>b)); //false
		System.out.println("a<b : " + (a<b)); //true
		System.out.println("a=b : " + (a==b)); //false
		System.out.println("a!=b : " + (a!=b)); //true
		
	}

}

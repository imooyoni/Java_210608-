package pks3;
//패키지명 작법: 무조건 소문자
public class BinaryTest {
//클래스명 작법 파스칼: 첫글자 무조건 대문자
	public static void main(String[] args) {
//변수명 작법 카멜: 맨앞소문자, 첫글자 대문자		
		int num=10;
		int bNum=0B1010;
		int oNum=012;
		int hNum=0xA;
//int: 정수(4바이트)
		System.out.println(num);
		System.out.println(bNum);
		System.out.println(oNum);
		System.out.println(hNum);
	}

}

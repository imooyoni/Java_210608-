package pks3;

public class BinaryTest2 {

	public static void main(String[] args) {
		
		int num1=16;
		//2진수
		int num2=0B10000;
		//8진수
		int num3=020;
		//16진수
		int num4=0X10;
				
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		System.out.println(num4);

	}

}

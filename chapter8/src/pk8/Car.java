package pk8;

public class Car {
	
	String company="현대자동차";
	String model="그랜저";
	String color="검정";
	int maxSpeed=350;
	int speed;
	
	void SetCompany (String c) {
		company=c;
	}
	void SetModel (String m) {
		model=m;
	}
	void SetColor (String cl) {
		color=cl;
	}
	void SetMaxSpeed (int ms) {
		maxSpeed=ms;
	}
	void SetSpeed (int s) {
		speed=s;
	}
}
